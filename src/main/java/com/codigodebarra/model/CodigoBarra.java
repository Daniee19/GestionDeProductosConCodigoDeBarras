package com.codigodebarra.model;

public class CodigoBarra {

    int id_barra;
    String nombre_barra;
    String tipo;

    public CodigoBarra() {
    }

    public int getId_barra() {
        return id_barra;
    }

    public void setId_barra(int id_barra) {
        this.id_barra = id_barra;
    }

    public String getNombre_barra() {
        return nombre_barra;
    }

    public void setNombre_barra(String nombre_barra) {
        this.nombre_barra = nombre_barra;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

}
